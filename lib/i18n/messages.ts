export const supportedLocales = ["fr", "en"] as const;

export type Locale = (typeof supportedLocales)[number];

export function isLocale(value: string | undefined | null): value is Locale {
  return value === "fr" || value === "en";
}

export function toIntlLocale(locale: Locale) {
  return locale === "fr" ? "fr-FR" : "en-US";
}

export function formatDate(locale: Locale, value: Date | string, options?: Intl.DateTimeFormatOptions) {
  return new Date(value).toLocaleDateString(toIntlLocale(locale), options);
}

export function formatDateTime(locale: Locale, value: Date | string) {
  return new Date(value).toLocaleString(toIntlLocale(locale));
}

